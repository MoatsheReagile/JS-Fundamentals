// Different data types
const name = "Reagile";               // String
let age = 20;                     // Number
const isStudent = true;            // Boolean
const notDefined = undefined;      // Undefined
const emptyValue = null;           // Null

// Print values
console.log("Name:", name);
console.log("Age:", age);
console.log("Is student:", isStudent);
console.log("Not defined:", notDefined);
console.log("Empty value:", emptyValue);

// Arithmetic operations
const sum = age + 5;
const product = age * 2;
const remainder = age % 3;

console.log("Sum (age + 5):", sum);
console.log("Product (age * 2):", product);
console.log("Remainder (age % 3):", remainder);

// Check data types
console.log("Type of name:", typeof name);
console.log("Type of age:", typeof age);
console.log("Type of isStudent:", typeof isStudent);
console.log("Type of notDefined:", typeof notDefined);
console.log("Type of emptyValue:", typeof emptyValue);
